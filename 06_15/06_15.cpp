#include <iostream> 
using namespace std; 
void main()
{
   int  a[5]={ 10,  20,  30,  40,  50};
   int  b[5]={ 60,  70,  80,  90, 100};  
   int  c[5]={110, 120, 130, 140, 150};  

   int  *p[3]={a, b, c};

   cout<<">> 각 1차원 배열의 첫번째 원소 출력 << \n";
   cout<<p[0][0]<<"\t"<<p[1][0]<<"\t"<<p[2][0]<<"\n\n";

   cout<<">> 각 1차원 배열의 두번째 원소 출력 << \n";
   cout<<p[0][1]<<"\t"<<p[1][1]<<"\t"<<p[2][1]<<"\n";
}
